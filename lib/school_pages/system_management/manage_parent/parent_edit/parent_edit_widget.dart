        );
      }
    } finally {
      if (mounted) setState(() => _saving = false);
    }
  }

  Future<void> _copyCredentials(DbProfile? parent) async {
    final email = parent?.loginEmail;
    final password = parent?.initialPassword;
    if (email == null || email.isEmpty) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('No login email on file for this parent.')),
      );
      return;
    }
    final text = password == null || password.isEmpty
        ? 'Email: $email'
        : 'Email: $email\nPassword: $password';
    await Clipboard.setData(ClipboardData(text: text));
    if (!mounted) return;
    ScaffoldMessenger.of(context).showSnackBar(
      const SnackBar(content: Text('Login credentials copied.')),
    );
  }

  Future<void> _resetPassword() async {
    if (_resettingPassword || _parentId == null) return;
    final confirm = await showDialog<bool>(
      context: context,
      builder: (ctx) => AlertDialog(
        title: const Text('Generate new password?'),
        content: const Text(
            'This replaces the current login password. Share the new password with the parent.'),
        actions: [
          TextButton(
            onPressed: () => Navigator.pop(ctx, false),
            child: const Text('Cancel'),
          ),
          TextButton(
            onPressed: () => Navigator.pop(ctx, true),
            child: const Text('Generate'),
          ),
        ],
      ),
    );
    if (confirm != true) return;

    setState(() => _resettingPassword = true);
    try {
      final result =
          await context.read<MockState>().resetProfilePassword(_parentId!);
      if (!mounted) return;
      if (result.error != null) {
