  Future<void> _saveChanges() async {
    if (_saving || _parentId == null) return;
    setState(() => _saving = true);
    try {
      await context.read<MockState>().updateProfileRecord(
            id: _parentId!,
            fullName: _model.textController1.text.trim(),
            phone: _model.textController3.text.trim(),
            nationalId: _model.textController2.text.trim(),
          );
      if (!mounted) return;
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('Parent updated.')),
      );
      context.goNamed(ParentManageWidget.routeName);
    } catch (e) {
      if (mounted) {
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(content: Text('Could not update parent: $e')),
        );
      }
    } finally {
      if (mounted) setState(() => _saving = false);
    }
  }

  Future<void> _deactivateParent() async {
    if (_saving || _parentId == null) return;
    final confirm = await showDialog<bool>(
      context: context,
      builder: (ctx) => AlertDialog(
        title: const Text('Deactivate parent?'),
